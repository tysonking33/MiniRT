#ifndef COLORS_H
#define COLORS_H

// Hexadecimal Color Definitions

// Red color
#define RED         0xFFFF0000 // RGBA: (255, 0, 0, 255)

// Green color
#define GREEN       0xFF00FF00 // RGBA: (0, 255, 0, 255)

// Blue color
#define BLUE        0xFF0000FF // RGBA: (0, 0, 255, 255)

// White color
#define WHITE       0xFFFFFFFF // RGBA: (255, 255, 255, 255)

// Black color
#define BLACK       0xFF000000 // RGBA: (0, 0, 0, 255)

// Yellow color
#define YELLOW      0xFFFFFF00 // RGBA: (255, 255, 0, 255)

// Cyan color
#define CYAN        0xFF00FFFF // RGBA: (0, 255, 255, 255)

// Magenta color
#define MAGENTA     0xFFFF00FF // RGBA: (255, 0, 255, 255)

// Gray color
#define GRAY        0xFF808080 // RGBA: (128, 128, 128, 255)

// Transparent color (if applicable)
#define TRANSPARENT 0x00000000 // RGBA: (0, 0, 0, 0)

#endif // COLORS_H
